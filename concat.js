//concat two arrays and give a new array
const array1 = [1,2,3];
const array2 = [4,5,6];

const newArray = array1.concat(array2);

console.log(newArray);






//we can concat mutiple arrays same time
const array3 = [1,2,3];
const array4 = [4,5,6];
const array5 = [7,8,9];

const modifiedArray = array3.concat(array4, array5);

console.log(modifiedArray);